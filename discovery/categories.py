def get_category_keywords(category_name: str) -> list:
    """Get keywords for a specific category"""
    if category_name not in CATEGORIES:
        raise ValueError(f"Unknown category: {category_name}")
    return CATEGORIES[category_name]["keywords"]


def get_all_categories() -> list:
    """Get list of all category names"""
    return list(CATEGORIES.keys())


def get_random_keyword(category_name: str = None) -> tuple[str, str]:
    """
    Get a random keyword from a category (or all categories).
    Returns (category_name, keyword)
    """
    import random

    if category_name:
        if category_name not in CATEGORIES:
            raise ValueError(f"Unknown category: {category_name}")
        keywords = CATEGORIES[category_name]["keywords"]
        return category_name, random.choice(keywords)
    else:
        cat = random.choice(list(CATEGORIES.keys()))
        kw = random.choice(CATEGORIES[cat]["keywords"])
        return cat, kw
